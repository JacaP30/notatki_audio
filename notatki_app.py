# G≈Ç√≥wna logika aplikacji
#====================================================================
# Ta czƒô≈õƒá aplikacji obs≈Çuguje interakcje u≈ºytkownika, takie jak nagrywanie audio, transkrypcja audio i wyszukiwanie notatek.
# U≈ºytkownik mo≈ºe nagraƒá notatkƒô audio, transkrybowaƒá jƒÖ do tekstu i zapisaƒá jƒÖ w bazie danych.
# U≈ºytkownik mo≈ºe r√≥wnie≈º wyszukiwaƒá notatki na podstawie tekstu.
add_tab, search_tab = st.tabs(["üéôÔ∏è Nagraj notatkƒô", "üîç Wyszukaj w notatkach"], width="stretch")
# Streamlit nie pozwala na bezpo≈õredniƒÖ konfiguracjƒô wyglƒÖdu przycisk√≥w (np. kolor√≥w, rozmiaru) przez parametry funkcji st.button.
# Mo≈ºesz jednak u≈ºyƒá komponentu st.markdown z HTML/CSS oraz unsafe_allow_html=True, aby stylizowaƒá przyciski, lub u≈ºyƒá bibliotek zewnƒôtrznych (np. streamlit-extras).
# Przyk≈Çad niestandardowego przycisku:
# st.markdown('<button style="background-color: #4CAF50; color: white; padding: 10px 24px; border: none; border-radius: 4px;">Niestandardowy przycisk</button>', unsafe_allow_html=True)
with add_tab:
    note_audio = audiorecorder(
        start_prompt= "‚è∫Ô∏è Nagraj swojƒÖ notatkƒô",
        stop_prompt="üî¥ Zatrzymaj nagrywanie",#icon_size=100,

    )
    # Sprawdzenie, czy nagrano audio
    #====================================================================
    # Je≈õli nagrano audio, jest ono konwertowane do formatu MP3 i przechowywane w sesji.
    # Nastƒôpnie sprawdzany jest MD5 tego audio, aby upewniƒá siƒô, ≈ºe nie zosta≈Ço ono zmienione.
    # Je≈õli MD5 siƒô zmieni, tekst notatki jest resetowany.
    # U≈ºytkownik mo≈ºe ods≈Çuchaƒá nagranie, transkrybowaƒá je do tekstu i edytowaƒá notatkƒô przed zapisaniem jej w bazie danych.
    if note_audio:
        audio = BytesIO()
        note_audio.export(audio, format="mp3")
        st.session_state["note_audio_bytes"] = audio.getvalue()

        curent_md5 = md5(st.session_state["note_audio_bytes"]).hexdigest()

        # Sprawdzenie, czy MD5 audio siƒô zmieni≈Ç
        #====================================================================
        # Je≈õli MD5 audio jest inny ni≈º poprzednio zapisany, 
        # resetujemy tekst notatki i ustawiamy nowy MD5.
        # Dziƒôki temu u≈ºytkownik mo≈ºe nagraƒá nowe audio i transkrybowaƒá je bez problem√≥w.
        if st.session_state["note_audio_bytes_md5"] != curent_md5:
            st.session_state["note_audio_text"] = ""
            st.session_state["note_text"] = ""
            st.session_state["note_audio_bytes_md5"] = curent_md5

        st.audio(st.session_state["note_audio_bytes"], format="audio/mp3")

        # Transkrypcja audio
        #====================================================================
        # Je≈õli u≈ºytkownik kliknie przycisk "Transkrybuj audio", 
        # audio jest transkrybowane do tekstu za pomocƒÖ modelu Whisper
        if st.button("üñãÔ∏è Transkrybuj audio"):
            st.session_state["note_audio_text"] = transcribe_audio(st.session_state["note_audio_bytes"])

        # Wy≈õwietlenie transkrybowanego tekstu
        #====================================================================
        # Je≈õli transkrybowany tekst jest dostƒôpny, 
        # jest on wy≈õwietlany w polu tekstowym, gdzie u≈ºytkownik mo≈ºe go edytowaƒá.
