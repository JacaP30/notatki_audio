# Funkcja do uzyskiwania klienta Qdrant
@st.cache_resource
def get_qdrant_client():
    return QdrantClient(
        url=env["QDRANT_URL"],
        api_key=env["QDRANT_API_KEY"],
    ) # path=":memory:" lub QdrantClient(url=env["QDRANT_URL"], api_key=env["QDRANT_API_KEY"])

# Funkcja sprawdzajƒÖca istnienie kolekcji i tworzƒÖca jƒÖ, je≈õli nie istnieje
#====================================================================
# Ta funkcja sprawdza, czy kolekcja o nazwie QDRANT_COLLECTION_NAME istnieje w bazie danych Qdrant.
# Je≈õli nie istnieje, tworzy jƒÖ z odpowiednimi parametrami wektor√≥w.
# Funkcja ta jest wywo≈Çywana przy starcie aplikacji, aby zapewniƒá, ≈ºe kolekcja jest gotowa do u≈ºycia.

def assure_db_collection_exists():
    qdrant_client = get_qdrant_client()
    if not qdrant_client.collection_exists(QDRANT_COLLECTION_NAME):
        print("Tworzƒô kolekcjƒô")
        qdrant_client.create_collection(
            collection_name=QDRANT_COLLECTION_NAME,
            vectors_config=VectorParams(
                size=EMBEDDING_DIM,
                distance=Distance.COSINE,
            ),
        )
    else:
        print("Kolekcja ju≈º istnieje")
# Funkcje do obs≈Çugi notatek
#====================================================================
# Te funkcje pozwalajƒÖ na uzyskiwanie wektora osadzenia dla tekstu, 
# dodawanie notatek do bazy danych oraz wyszukiwanie notatek w bazie danych.
def get_embedding(text):
    openai_client = get_openai_client()
    result = openai_client.embeddings.create(
        input=[text],
        model=EMBEDDING_MODEL,
        dimensions=EMBEDDING_DIM,
    )

    return result.data[0].embedding

# Funkcja do dodawania notatki do bazy danych
#====================================================================
# Ta funkcja dodaje notatkƒô do bazy danych Qdrant. 
# Przyjmuje tekst notatki jako argument, oblicza wektor osadzenia dla tego tekstu 
# i zapisuje go w kolekcji QDRANT_COLLECTION_NAME wraz z tekstem notatki.
def add_note_to_db(note_text):
    qdrant_client = get_qdrant_client()
    points_count = qdrant_client.count(
        collection_name=QDRANT_COLLECTION_NAME,
        exact=True,
    )
    qdrant_client.upsert(
        collection_name=QDRANT_COLLECTION_NAME,
        points=[
            PointStruct(
                id=points_count.count + 1,
                vector=get_embedding(text=note_text),
                payload={
                    "text": note_text,
                },
            )
        ]
    )

# Funkcja do listowania notatek z bazy danych
#====================================================================
# Ta funkcja zwraca listƒô notatek z bazy danych Qdrant. 
# Je≈õli podano zapytanie, wyszukuje notatki na podstawie wektora osadzenia zapytania.
# W przeciwnym razie zwraca ostatnie 10 notatek.
def list_notes_from_db(query=None):
    qdrant_client = get_qdrant_client()
    if not query:
        notes = qdrant_client.scroll(collection_name=QDRANT_COLLECTION_NAME, limit=10)[0]
        result = []
        for note in notes:
            text = note.payload["text"] if note.payload and "text" in note.payload else ""
            result.append({
                "text": text,
                "score": None,
            })

        return result

    # Je≈õli podano zapytanie, wyszukujemy notatki na podstawie wektora osadzenia zapytania
    #====================================================================
    # Ta czƒô≈õƒá kodu wyszukuje notatki w bazie danych Qdrant na podstawie wektora osadzenia zapytania.
    # Zwraca listƒô notatek z ich tekstem i wynikiem wyszukiwania.   
    else:
        notes = qdrant_client.search(
            collection_name=QDRANT_COLLECTION_NAME,
            query_vector=get_embedding(text=query),
            limit=10,
        )
        result = []
        for note in notes:
            text = note.payload["text"] if note.payload and "text" in note.payload else ""
            result.append({
                "text": text,
                "score": note.score,
            })

        return result


